   private void create1()
   {
      ArrayList<String> locationLists1 = new ArrayList();
       for (Airliner a : airliners.getAirliners()) {
           locationLists1.add(a.getAirlinerNames());
       }
      ArrayListComboBoxModel model = new ArrayListComboBoxModel(locationLists1);
      Airlinercombo.setModel(model);
   }
   
   private void create2()
   {
      ArrayList<String> locationLists1 = new ArrayList();
       for (Airplane a : airliners.SearchAirliners(Airlinercombo.getSelectedItem().toString()).getAirList()) {
           locationLists1.add(a.getSerialNum());
       }
      ArrayListComboBoxModel model = new ArrayListComboBoxModel(locationLists1);
      airplanecombo.setModel(model);
   }

