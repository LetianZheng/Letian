    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
           Flight f=new Flight();
           DateFormat format1 = new SimpleDateFormat("yyyy-MM-dd");  
           
           if (txtFlightno.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "FlightNO Can't be null");
            return;
        }else f.setFlightID(txtFlightno.getText());
           
           if (txtfrom.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "From City Can't be null");
            return;
        }else f.setFromAirport(txtfrom.getText());
           
           if (txtTo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "To City Can't be null");
            return;
        }else f.setToAirport(txtTo.getText());
           
           
           f.setTime(TimeCombox.getSelectedItem().toString());
           f.setAirliner(Airlinercombo.getSelectedItem().toString());
           try {
            f.setArriveDate(format1.parse(txtyear.getText()));
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Date format is not right");
            return;
        }
           
           f.setAirplane(airplanecombo.getSelectedItem().toString());
       
           flights.addflight(f);
           JOptionPane.showMessageDialog(this, "Success");
        // TODO add your handling code here:
    }//GEN-LAST:event_createBtnActionPerformed

