    private void availBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_availBtnActionPerformed
        // TODO add your handling code here:
        int Selectedticket = ticketTable.getSelectedRow();
        if (Selectedticket< 0)
        {
        JOptionPane.showMessageDialog(null,"Please select row from the Table first to view","Warning",JOptionPane.WARNING_MESSAGE);
        return;
                }
        else{
         
            if (ticketTable.getValueAt(Selectedticket, 2)!=null) {
                JOptionPane.showMessageDialog(this, "Already have seat");
                return;
            }
            selectedCustomerNo=(String)ticketTable.getValueAt(Selectedticket,0);
            SelectedFlightNo=(String)ticketTable.getValueAt(Selectedticket,1);
            flight=flightlist.searchFlightbyNo(SelectedFlightNo);
        }
        populateTable(flight);
    }//GEN-LAST:event_availBtnActionPerformed
    
    public String getType(int column){
        StringBuilder a = new StringBuilder();
        switch(column){
            case 0:
                a.append("Left Window");
                break;
            case 1:
                a.append("Left Middle");
                break;
            case 2:
                a.append("Left Aisle"); 
                break;
            case 3:
                a.append("Right Aisle");
                break;
            case 4:
                a.append("Right Middle");  
                break;
            case 5:
                a.append("Right Window"); 
                break;
                 
        }
        return a.toString();
        
    }
    
    public void populateTable(Flight flight){
        DefaultTableModel dtm = (DefaultTableModel)seatTable.getModel();
        dtm.setRowCount(0);
        DateFormat df = DateFormat.getDateInstance(DateFormat.SHORT);
        for (int i = 0; i < 25; i++) {
            for (int j = 0; j < 6; j++) {
                Object row[] = new Object[4];
                row[0] = flight.nameoftheseat(i+1,j+1);
                row[1] = i+1;
                row[2] = j+1;
                row[3] = getType(j);
                System.out.println(getType(1));
                if(flight.isavaliable(i,j)){
                   dtm.addRow(row);
               
                }
               
           }
       }
        
    }
    
    private void reserveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = seatTable.getSelectedRow();
        if (selectedRow >= 0 ){
            int row = (Integer)seatTable.getValueAt(selectedRow,1);
            int column = (Integer)seatTable.getValueAt(selectedRow,2);
            flight.setAvail(row-1, column-1);  
            populateTable(flight);            
            Ticket ticket = tickets.SearchTicket(selectedCustomerNo,SelectedFlightNo);
            ticket.setSeatName((String)seatTable.getValueAt(selectedRow,0));
            JOptionPane.showMessageDialog(this, "Book Successfully");
            populateTicket();
        }
        else
            JOptionPane.showMessageDialog(null, "Please select any rows");
        
    }//GEN-LAST:event_reserveBtnActionPerformed

