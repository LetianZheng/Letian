    public void populateTicket(){
        DefaultTableModel dtm = (DefaultTableModel)ticketTable.getModel();
        dtm.setRowCount(0);
        for(Ticket ticket: tickets.getTickets()){
            Object row[] = new Object[5];
            row[0] = ticket.getCustomerID();
            row[1] = ticket.getFlightID();
            row[2] = ticket.getSeatName();
            dtm.addRow(row);
            
        }
       }
        

